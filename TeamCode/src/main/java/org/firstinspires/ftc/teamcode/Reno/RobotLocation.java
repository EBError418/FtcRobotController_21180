package org.firstinspires.ftc.teamcode.Reno;

public class RobotLocation {
    public double x;
    public double y;
    public double z;
    public double angle;

    public RobotLocation(double x, double y, double z, double angle)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.angle = angle;
    }

}
